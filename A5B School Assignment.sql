SELECT A.EMP_NUM,EMP_LNAME,SUM(ROUND(ASSIGN_HOURS,1)) AS SUMOFASSIGN_HOURS, SUM(ROUND(ASSIGN_CHARGE,1)) AS SUMOFASSIGN_CHARGE
FROM ASSIGNMENT AS A INNER JOIN EMPLOYEEC AS E ON A.EMP_NUM = E.EMP_NUM
GROUP BY A.EMP_NUM, EMP_LNAME

