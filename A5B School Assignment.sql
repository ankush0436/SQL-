
SELECT EMP_NUM, EMP_LNAME, EMP_FNAME, EMP_INITIAL
FROM EMPLOYEEC
WHERE EMP_LNAME LIKE 'SMITH%'
ORDER BY EMP_NUM 


--Question 2
SELECT PROJ_NAME,CAST(PROJ_VALUE AS DECIMAL(11,2)) AS PROJ_VALUE, CAST(ROUND(PROJ_BALANCE,1) AS DECIMAL(11,2)) AS PROJ_BALANCE,EMP_LNAME,EMP_FNAME,EMP_INITIAL,J.JOB_CODE,JOB_DESCRIPTION,JOB_CHG_HOUR
FROM JOB AS J INNER JOIN EMPLOYEEC AS E ON J.JOB_CODE = E.JOB_CODE
	  INNER JOIN PROJECT AS P ON E.EMP_NUM = P.EMP_NUM
ORDER BY PROJ_VALUE ASC

